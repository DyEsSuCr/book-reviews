import app from '@/app'
import { dbConnect } from '@/database/connect'

const main = async () => {
  try {
    await dbConnect()

    app.listen(app.get('port'), () => {
      console.log(`🆗✅🆗 Server on port ${app.get('port')} 🆗✅🆗`)
    })
  } catch (err) {
    console.error(`🛑⛔ Not connection database ❗${err}❗ ⛔🛑`)
  }
}

main()